package ch.bfh.bti7081.s2018.black.pms.model;

/**
 * Clinic item class
 */
public class ClinicItem {
	
	// Name of the clinic
	private String name;

	public ClinicItem() {
	}
	
	/**
	 * Getter for the name
	 * @return the name of the clinic
	 */
	public String getName() {
		return this.name;
	}

	/**
	 * Setter for the name
	 * @param the name of the clinic
	 */
	public void setName(String name) {
		this.name = name;
	}
}
